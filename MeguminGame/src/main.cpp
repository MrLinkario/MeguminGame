		  // Collider de Megumin
		AbstractModel::OBB meguminCollider;
		glm::mat4 modelmatrixColliderMegumin = glm::mat4(modelMatrixMegumin);
		modelmatrixColliderMegumin = glm::rotate(modelmatrixColliderMegumin, glm::radians(-90.0f), glm::vec3(1, 0, 0));
